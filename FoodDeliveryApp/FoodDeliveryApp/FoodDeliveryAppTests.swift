// MARK: - Тесты моделей данных

@Suite("Тесты моделей ресторана и блюд")
struct МодельТесты {

    @Test("Проверка создания ресторана")
    func тестРесторана() {
        let ресторан = Ресторан(название: "Тест", эмодзи: "🍕", кухня: "Тестовая", блюда: [])
        #expect(ресторан.название == "Тест")
        #expect(ресторан.эмодзи == "🍕")
    }
    
    @Test("Проверка создания блюда")
    func тестБлюда() {
        let блюдо = Блюдо(название: "Пицца", цена: 100, описание: "Вкусно", категория: .основное, эмодзи: "🍕")
        #expect(блюдо.название == "Пицца")
        #expect(блюдо.цена == 100)
    }
    
    @Test("Проверка добавления в корзину")
    func тестКорзины() {
        var корзина: [ТоварВКорзине] = []
        let блюдо = Блюдо(название: "Бургер", цена: 50, описание: "Сочный", категория: .основное, эмодзи: "🍔")
        корзина.append(ТоварВКорзине(блюдо: блюдо, количество: 1))
        #expect(корзина.count == 1)
    }
    
    @Test("Проверка общей суммы корзины")
    func тестОбщейСуммы() {
        let блюдо1 = Блюдо(название: "А", цена: 100, описание: "", категория: .основное, эмодзи: "🍕")
        let блюдо2 = Блюдо(название: "Б", цена: 50, описание: "", категория: .основное, эмодзи: "🍔")
        let корзина = [
            ТоварВКорзине(блюдо: блюдо1, количество: 2),
            ТоварВКорзине(блюдо: блюдо2, количество: 1)
        ]
        let сумма = корзина.reduce(0) { $0 + ($1.блюдо.цена * Double($1.количество)) }
        #expect(сумма == 250)
    }
    
    @Test("Проверка статуса заказа")
    func тестСтатуса() {
        let статус = СтатусЗаказа.готовится
        #expect(статус.rawValue == "👨‍🍳 Готовится")
    }
    
    @Test("Проверка что корзина пуста")
    func тестПустойКорзины() {
        let корзина: [ТоварВКорзине] = []
        #expect(корзина.isEmpty)
    }
    
    @Test("Проверка увеличения количества")
    func тестУвеличенияКоличества() {
        let блюдо = Блюдо(название: "Пицца", цена: 100, описание: "", категория: .основное, эмодзи: "🍕")
        var товар = ТоварВКорзине(блюдо: блюдо, количество: 1)
        товар.количество = 3
        #expect(товар.количество == 3)
    }
}

